module slave_bridge_DMAs  (
input logic HCLK,
input logic HRESETn,
input logic HSEL_DMAS,
input logic [31:0] HADDR_DMAS,
input logic [1:0] HTRANS_DMAS,
input logic HWRITE_DMAS,
input logic [2:0] HSIZE_DMAS,
input logic [31:0] HWDATA_DMAS,
output logic [31:0] HRDATA_DMAS,
output logic HREADYOUT_DMAS,
output logic HRESP_DMAS,
output logic [31:0] C_REG_CPU_CMD,
output logic [31:0] C_REG_CPU_RECEIVE_FLAG,
input logic [31:0] S_REG_PREPARE_FINISH,
input logic [31:0] S_REG_INPUT_TOKEN_LEN,
input logic [31:0] S_REG_CPU_CMD_DONE,
input logic [31:0] S_REG_CPU_WRITE_DONE,
output logic [31:0] C_REG_CLEAR_IRQ
);

logic        act_w, act_r;
logic [31:0] addr_w, addr_r;
logic [ 3:0] wen_w, wen_r;
logic [13:0] addr_dec;

logic [31:0] w_CPU_CMD;
logic [31:0] r_CPU_CMD;
assign C_REG_CPU_CMD = r_CPU_CMD;

logic [31:0] w_CPU_RECEIVE_FLAG;
logic [31:0] r_CPU_RECEIVE_FLAG;
assign C_REG_CPU_RECEIVE_FLAG = r_CPU_RECEIVE_FLAG;





logic w_CLEAR_IRQ;
logic r_CLEAR_IRQ;
assign C_REG_CLEAR_IRQ = r_CLEAR_IRQ;

assign addr_dec = addr_r[15:2];
always_comb begin
    act_w  = (HSEL_DMAS && HTRANS_DMAS == 2'b10);
    addr_w = (HSEL_DMAS && HTRANS_DMAS == 2'b10) ? HADDR_DMAS : addr_r;
    wen_w  = (HSEL_DMAS && HTRANS_DMAS == 2'b10) ? HWRITE_DMAS : wen_r;
end

always_comb begin
HRDATA_DMAS    = 32'd0;
HREADYOUT_DMAS = 1'b1;
HRESP_DMAS     = 1'b0;
w_CPU_CMD = '0;
w_CPU_RECEIVE_FLAG = '0;
w_CLEAR_IRQ = '0;
if(act_r) begin
if(wen_r) begin
if(addr_dec == 0) begin w_CPU_CMD = HWDATA_DMAS; end
if(addr_dec == 1) begin w_CPU_RECEIVE_FLAG = HWDATA_DMAS; end
if(addr_dec == 6) begin w_CLEAR_IRQ = HWDATA_DMAS; end
end else begin
if(addr_dec == 2) begin HRDATA_DMAS = S_REG_PREPARE_FINISH; end
if(addr_dec == 3) begin HRDATA_DMAS = S_REG_INPUT_TOKEN_LEN; end
if(addr_dec == 4) begin HRDATA_DMAS = S_REG_CPU_CMD_DONE; end
if(addr_dec == 5) begin HRDATA_DMAS = S_REG_CPU_WRITE_DONE; end
end
end
end

always_ff@(posedge HCLK or negedge HRESETn) begin
if(~HRESETn) begin
act_r <= '0;
addr_r <= '0;
wen_r <= '0;
r_CPU_CMD <= '0;
r_CPU_RECEIVE_FLAG <= '0;
r_CLEAR_IRQ <= '0;
end else begin
act_r <= act_w;
addr_r <= addr_w;
wen_r <= wen_w;
r_CPU_CMD <= w_CPU_CMD;
r_CPU_RECEIVE_FLAG <= w_CPU_RECEIVE_FLAG;
r_CLEAR_IRQ <= w_CLEAR_IRQ;
end
end

endmodule
